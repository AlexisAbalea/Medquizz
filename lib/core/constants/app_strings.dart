  static String getMotivationalMessage(double percentage) {
    if (percentage == 100) {
      return 'Parfait ! 🎉 Score parfait !';
    } else if (percentage >= 90) {
      return 'Excellent ! 🌟 Vous maîtrisez le sujet !';
    } else if (percentage >= 80) {
      return 'Très bien ! 👏 Beau travail !';
    } else if (percentage >= 70) {
      return 'Bien ! 👍 Continuez comme ça !';
    } else if (percentage >= 60) {
      return 'Pas mal ! 💪 Encore un effort !';
    } else if (percentage >= 50) {
      return 'Moyen. 📚 Révisez un peu plus !';
    } else {
      return 'Courage ! 💪 Révisez et réessayez !';
    }
  }
